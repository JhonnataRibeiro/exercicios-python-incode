# Escreva um script que solicite ao usuário que insira a base e a altura do triângulo e calcule uma área desse triângulo (área = 0,5 xbxh).

base = float(input("Informe a base do triângulo: "))
altura = float(input("Informe a altura do triângulo: "))
area = (base * altura) / 2

print(f"A área do triângulo é {area}")