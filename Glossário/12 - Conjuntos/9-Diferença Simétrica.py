# Em Diferença Simétrica vai mostrar apenas os itens em incomum de ambos conjuntos, não mostra o que são iguais

conjunto1 = {"a", "k", "b", "w", "c", "y"}
conjunto2 = {"k", "d", "w", "e", "y", "f"}

diferenca_simetrica = conjunto1 ^ conjunto2 

print(diferenca_simetrica)



conjunto1 = {2, 9, 4, 6, 8}
conjunto2 = {1, 9, 3, 5, 7}

diferenca_simetrica = conjunto1 ^ conjunto2

print(diferenca_simetrica)